namespace StoreApp.MVVM.Model
{
    class Administrator : Employee
    {
        private string _login;
        private string _password;

        public string Login
        {
            get => _login;
            set
            {
                if (value == _login) return;
                _login = value;
                OnPropertyChanged();
            }
        }
        public string Password
        {
            get => _password;
            set
            {
                if (value == _password) return;
                _password = value;
                OnPropertyChanged();
            }
        }
    }
}
